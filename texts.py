# PDF → markdown
# ---------------------------------------------------------------------------

def pdf_to_markdown(pdf_path: Path) -> Path:
    """
    Extract text from a PDF and save as a sibling .md file.
    Simple page-by-page text dump — preserves structure better than nothing,
    keeps the file readable for LLM ingestion.
    """
    if not pdf_path.exists():
        raise FileNotFoundError(pdf_path)
    if pdf_path.suffix.lower() != ".pdf":
        raise ValueError("not a PDF")

    doc = pdf_safety.open_pdf_safely(pdf_path)
    parts: list[str] = [f"# {pdf_path.stem}\n",
                        f"*Source: {pdf_path.name}, {doc.page_count} pages*\n", "---\n"]
    for pno, page in enumerate(doc, 1):
        text = page.get_text("text").strip()
        if not text:
            continue
        text = re.sub(r"[ \t]+\n", "\n", text)
        text = re.sub(r"\n{3,}", "\n\n", text)
        parts.append(f"\n## Page {pno}\n\n{text}\n")
    doc.close()

    out = pdf_path.with_suffix(".md")
    out.write_text("\n".join(parts), encoding="utf-8")
    return out


# ---------------------------------------------------------------------------
# Textbook chapter detection
# ---------------------------------------------------------------------------

# "Chapter 3", "CHAPTER ONE", "Unit IV", "Section 2:" — a heading line, not a
# mid-sentence mention, so this only matches at the START of a (stripped) line.
_CHAPTER_RE = re.compile(
    r"^(?:chapter|unit|section)\s+([0-9]{1,3}|[ivxlcdm]{1,7}|[a-z]+)\b[:.\-—]?\s*(.*)$",
    re.IGNORECASE,
)
# pdf_to_markdown() tags every page with exactly this marker (see above).
_PAGE_MARKER_RE = re.compile(r"^##\s*Page\s+(\d+)\s*$")


def detect_chapters(pdf_path: Path, md_text: str) -> dict:
    """
    Find chapter boundaries for a shared textbook PDF, for the "generate
    questions from one chapter" feature. Two strategies, tried in order:

    1. The PDF's own embedded outline/bookmarks (`get_toc()`) — exact page
       numbers, used as-is when present.
    2. A regex scan of `md_text` (the page-tagged dump `pdf_to_markdown()`
       produces) for "Chapter N" / "Unit N" / "Section N" style heading
       lines, tracking the most recent `## Page N` marker to assign each
       match's starting page. Needed because most textbook PDFs have no
       embedded bookmarks at all.

    If neither finds anything, the caller should fall back to letting the
    user type in chapter boundaries by hand.

    Returns {"chapters": [{"title","start_page","end_page"}, ...],
             "source": "toc"|"text-scan"|"manual", "needs_manual_chapters": bool}
    """
    raw: list[tuple[str, int]] = []  # (title, start_page)
    last_page = 0

    try:
        doc = pdf_safety.open_pdf_safely(pdf_path)
        toc = doc.get_toc()  # [[level, title, page], ...]
        last_page = doc.page_count
        doc.close()
    except Exception:
        toc = []

    if toc:
        top_level = min(lvl for lvl, _, _ in toc)
        raw = [(title.strip(), page) for lvl, title, page in toc if lvl == top_level]
        source = "toc"
    else:
        cur_page = 1
        for line in md_text.splitlines():
            stripped = line.strip()
            pm = _PAGE_MARKER_RE.match(stripped)
            if pm:
                cur_page = int(pm.group(1))
                last_page = max(last_page, cur_page)
                continue
            if _CHAPTER_RE.match(stripped):
                raw.append((stripped, cur_page))
        source = "text-scan"

    if not raw:
        return {"chapters": [], "source": "manual", "needs_manual_chapters": True}

    chapters = []
    for i, (title, start) in enumerate(raw):
        end = raw[i + 1][1] - 1 if i + 1 < len(raw) else max(last_page, start)
        chapters.append({"title": title, "start_page": start,
                          "end_page": max(end, start)})
    return {"chapters": chapters, "source": source, "needs_manual_chapters": False}


def read_source_text(event: Event, name: str) -> str:
    """
    Load the markdown form of a source by its base name.
    - "wiki.md" -> read wiki.md
    - "foo.pdf" -> read foo.md (PDF must have been processed first)
    - "foo.md"  -> read foo.md
    """
    d = texts_dir(event)
    p = d / name
    if name.lower().endswith(".pdf"):
        p = d / (Path(name).stem + ".md")
        if not p.exists():
            raise FileNotFoundError(
                f"{name} has not been converted to markdown yet — process it first.")
    if not p.exists():
        raise FileNotFoundError(p)
    return p.read_text(encoding="utf-8")
